// A scripted JobManagerApi: status() walks a fixed sequence of statuses (holding on
// the last entry once exhausted), so runMineCli's poll loop can be driven
// deterministically — no real 5s sleeps in tests. Paired with CliDeps.pollIntervalMs:
// 0 (this test suite's chosen seam for making the poll loop testable — see report),
// which collapses the loop's inter-poll delay so a multi-poll script still resolves
// instantly.
class ScriptedJobManager implements JobManagerApi {
  startCalls: { config: MineConfig; deps: JobDeps }[] = [];
  private idx = 0;
  constructor(private readonly statuses: JobStatus[], private readonly res: JobResult | null = null) {}

  start(config: MineConfig, deps: JobDeps): { jobId: string; resumed: boolean } {
    this.startCalls.push({ config, deps });
    return { jobId: "job-1", resumed: false };
  }
  status(): JobStatus {
    const s = this.statuses[Math.min(this.idx, this.statuses.length - 1)]!;
    this.idx++;
    return s;
  }
  async cancel(): Promise<{ checkpointed: boolean }> {
    return { checkpointed: false };
  }
  result(): JobResult | null {
    return this.res;
  }
}

function readyStatus(overrides: Partial<JobStatus> = {}): JobStatus {
  return { state: "ready-for-preview", jobId: "job-1", stage: "ready-for-preview", counters: {}, warnings: [], tokensSpentUsd: 0.42, ...overrides };
}

function streams() {
  const stdout = new PassThrough();
  const stderr = new PassThrough();
  const outChunks: string[] = [];
  const errChunks: string[] = [];
  stdout.on("data", (c) => outChunks.push(String(c)));
  stderr.on("data", (c) => errChunks.push(String(c)));
  return { stdout, stderr, out: () => outChunks.join(""), err: () => errChunks.join("") };
}

function baseArgv(repo: string, repoPath: string, extra: string[] = []): string[] {
  return [repo, "--intent", "test intent", "--repo-path", repoPath, ...extra];
}

