test("happy flow: polls to ready, prints preview, writes after confirm=true, exit 0", async () => {
  const repoPath = await tmpRepo();
  const rule = mkRule();
  const provenance = mkProvenance([rule]);
  const draft = mkDraft([]);
  const manager = new ScriptedJobManager(
    [
      { state: "running", jobId: "job-1", stage: "fetching", counters: { prsFetched: 1 }, warnings: [], tokensSpentUsd: 0.1 },
      readyStatus({ tokensSpentUsd: 0.5 }),
    ],
    { draft, provenance, contested: [] },
  );
  const { stdout, stderr, out, err } = streams();
  const confirm = vi.fn(async () => true);

  const code = await runMineCli(baseArgv("octo/repo", repoPath), {
    makeDeps,
    manager,
    stdout,
    stderr,
    confirm,
    pollIntervalMs: 0,
  });

  expect(code).toBe(0);
  expect(confirm).toHaveBeenCalledTimes(1);
  expect(confirm.mock.calls[0]![0]).toMatch(/write/i);
  expect(confirm.mock.calls[0]![0]).toMatch(/AGENTS\.md/);

  // preview printed to stdout before the write
  expect(out()).toContain("Always write tests first");

  // emitDraft was actually called (real, unmocked implementation) and wrote the file
  expect(emitDraft).toHaveBeenCalledTimes(1);
  const written = await readFile(join(repoPath, "AGENTS.md"), "utf8");
  expect(written).toContain("Always write tests first");

  // "wrote:" status line went to stderr, not stdout (stdout is draft-only for piping)
  expect(err()).toMatch(/wrote.*AGENTS\.md/);
  expect(err()).toContain(join(repoPath, "AGENTS.md"));
  expect(out()).not.toMatch(/wrote/i);

  // stdout contains draft content, no trailing status lines
  expect(out()).toContain("Always write tests first");
  expect(out()).not.toMatch(/wrote|not written|dry.run/i);

  // progress line(s) went to stderr
  expect(err()).toMatch(/fetching/);
});

