// ---- happy path: mine -> status -> preview -> write -------------------------

test("mine -> status -> preview -> write happy path", async () => {
  const repoPath = await tmpRepo();
  const manager = new StubJobManager();
  const client = await connectedClient(manager);

  const mineRes = await client.callTool({
    name: "mine",
    arguments: { repo: "octo/repo", intent: "help new contributors", repoPath },
  });
  expect(mineRes.isError).toBeFalsy();
  const { jobId, resumed } = JSON.parse(textOf(mineRes));
  expect(typeof jobId).toBe("string");
  expect(resumed).toBe(false);

  // mine wired the deps as spec'd: a transport function, a provider with spentUsd(),
  // repoPath/stateDir defaulted.
  expect(manager.startCalls).toHaveLength(1);
  const { deps } = manager.startCalls[0]!;
  expect(typeof deps.transport).toBe("function");
  expect(typeof deps.provider.spentUsd).toBe("function");
  expect(deps.repoPath).toBe(repoPath);
  expect(deps.stateDir).toBe(join(repoPath, ".prlore"));

  const statusRes = await client.callTool({ name: "status", arguments: { jobId } });
  const status = JSON.parse(textOf(statusRes));
  expect(status.state).toBe("running");
  expect(status.jobId).toBe(jobId);

  // fake pipeline reaches ready-for-preview
  const rule = mkRule();
  const provenance = mkProvenance([rule]);
  manager.completeToReady({ draft: mkDraft([]), provenance, contested: [] });

  const previewRes = await client.callTool({ name: "preview", arguments: {} });
  expect(previewRes.isError).toBeFalsy();
  const preview = JSON.parse(textOf(previewRes));
  expect(preview.markdown).toContain("Always write tests first");
  expect(preview.stats).toEqual({ rulesIncluded: 1, droppedCount: 0, contestedCount: 0 });
  expect(preview.targetExists).toBe(false);
  expect(preview.wouldRefuse).toBe(false);
  expect(typeof preview.confirmToken).toBe("string");

  const writeRes = await client.callTool({ name: "write", arguments: { confirmToken: preview.confirmToken } });
  expect(writeRes.isError).toBeFalsy();
  const writeOut = JSON.parse(textOf(writeRes));
  expect(writeOut.pathsWritten).toContain(join(repoPath, "AGENTS.md"));

  const written = await readFile(join(repoPath, "AGENTS.md"), "utf8");
  expect(written).toContain("Always write tests first");
});

