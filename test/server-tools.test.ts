// ---- contested-header strip must anchor to the LAST occurrence ----------------
//
// A rule's `statement` is untrusted, PR-derived content — it can legally contain
// the literal contested-header text as ordinary prose. render.ts guarantees the
// REAL "## Needs your call (contested)" heading is always the LAST occurrence in
// a rendered draft (at most one genuine heading is ever emitted). finalizeDraft
// must anchor its strip to that last occurrence, not the first indexOf match, or
// a poisoned statement silently truncates every rule and section after it.

test("a rule statement containing the literal contested-header text does not truncate the draft early", async () => {
  const repoPath = await tmpRepo();
  const manager = new StubJobManager();
  const client = await connectedClient(manager);

  await client.callTool({ name: "mine", arguments: { repo: "octo/repo", intent: "x", repoPath } });

  const poisonRule = mkRule({ id: "poison", statement: "Mentions ## Needs your call (contested) inline text" });
  const laterRule = mkRule({ id: "later", statement: "Always run linters before commit" });
  const contested = [mkContested({ id: "c1", statement: "Use tabs, not spaces" })];

  // Hand-built to mirror render.ts's actual output shape: the poisoned bullet
  // (containing the literal header substring) lands ahead of a further real rule
  // bullet, which in turn lands ahead of the genuine (last) contested header.
  const draft =
    [
      "<!-- generated by prlore -->",
      "# Conventions",
      "",
      "auto-generated",
      "",
      "## Testing",
      `- **${poisonRule.statement}**`,
      `- **${laterRule.statement}**`,
      "",
      "## Needs your call (contested)",
      `- ${contested[0]!.statement} — conflicting guidance`,
    ].join("\n") + "\n";

  manager.completeToReady({
    draft,
    provenance: mkProvenance([poisonRule, laterRule], contested),
    contested,
  });

  const preview = JSON.parse(textOf(await client.callTool({ name: "preview", arguments: {} })));

  const writeRes = await client.callTool({ name: "write", arguments: { confirmToken: preview.confirmToken } });
  expect(writeRes.isError).toBeFalsy();

  const written = await readFile(join(repoPath, "AGENTS.md"), "utf8");
  // Both real rules survive — a `lastIndexOf` anchor keeps the poisoned bullet's
  // embedded header text from being mistaken for the real (last) header.
  expect(written).toContain(poisonRule.statement);
  expect(written).toContain(laterRule.statement);
  // The real (unresolved) contested item is still stripped as usual.
  expect(written).not.toContain("Use tabs, not spaces");
  // Only the poison rule's embedded occurrence remains; the genuine heading is gone.
  const headerOccurrences = written.split("## Needs your call (contested)").length - 1;
  expect(headerOccurrences).toBe(1);
});

// ---- contested-header strip must be line-anchored, not lastIndexOf ------------
//
// A CONTESTED item's `statement` is untrusted, model-derived text too, and its
// bullet is rendered AFTER the genuine header (contested bullets always follow
// the "## Needs your call (contested)" heading in render.ts's output). If a
// contested statement embeds the literal header substring, that occurrence
// sits LATER in the draft than the real header — so lastIndexOf anchors to the
// poisoned occurrence instead, and the strip keeps the genuine header plus
// every unresolved contested bullet ahead of the poisoned one. finalizeDraft
// must anchor to a full-line match of the header instead.

test("a contested item's statement embedding the literal header text does not defeat the strip", async () => {
  const repoPath = await tmpRepo();
  const manager = new StubJobManager();
  const client = await connectedClient(manager);

  await client.callTool({ name: "mine", arguments: { repo: "octo/repo", intent: "x", repoPath } });

  const normalContested = mkContested({ id: "c1", statement: "Use tabs, not spaces" });
  const poisonContested = mkContested({
    id: "c2",
    statement: "Mentions ## Needs your call (contested) inline as prose",
    reason: "poisoned reason",
  });
  const contested = [normalContested, poisonContested];

  // Hand-built to mirror render.ts's actual output shape: the genuine header
  // comes first, and BOTH contested bullets — including the poisoned one whose
  // statement embeds the literal header substring — follow it.
  const draft =
    [
      "<!-- generated by prlore -->",
      "# Conventions",
      "",
      "auto-generated",
      "",
      "## Testing",
      "- **Always write tests first**",
      "",
      "## Needs your call (contested)",
      `- ${normalContested.statement} — conflicting guidance`,
      `- ${poisonContested.statement} — ${poisonContested.reason}`,
    ].join("\n") + "\n";

  manager.completeToReady({
    draft,
    provenance: mkProvenance([mkRule()], contested),
    contested,
  });

  const preview = JSON.parse(textOf(await client.callTool({ name: "preview", arguments: {} })));

  // No resolveContested at all: every contested item is unresolved and must be
  // fully stripped — including the genuine header itself.
  const writeRes = await client.callTool({ name: "write", arguments: { confirmToken: preview.confirmToken } });
  expect(writeRes.isError).toBeFalsy();

  const written = await readFile(join(repoPath, "AGENTS.md"), "utf8");
  expect(written).not.toContain("Needs your call (contested)");
  expect(written).not.toContain("Use tabs, not spaces");
  expect(written).not.toContain("Mentions ## Needs your call");
  expect(written).toContain("Always write tests first");
});

// ---- write token single-use guard must close the TOCTOU window ----------------
//
// The write handler checks previewState/confirmToken, awaits emitDraft, then
// clears previewState. Two concurrent write calls sharing the same token can
// both pass the (pre-await) check before either clears it. Firing two calls
// back-to-back without awaiting the first exercises exactly that interleaving:
// this SDK's in-memory transport dispatches a tool call synchronously up to the
// handler's first internal `await`, so the fix (clearing previewState BEFORE the
// `await emitDraft(...)`) must make the second call's check fail synchronously.

test("two concurrent write calls sharing one token: exactly one succeeds, the other gets a stale-token error", async () => {
  const repoPath = await tmpRepo();
  const manager = new StubJobManager();
  const client = await connectedClient(manager);

  await client.callTool({ name: "mine", arguments: { repo: "octo/repo", intent: "x", repoPath } });
  manager.completeToReady({ draft: mkDraft([]), provenance: mkProvenance([mkRule()]), contested: [] });
  const preview = JSON.parse(textOf(await client.callTool({ name: "preview", arguments: {} })));

  const call = () => client.callTool({ name: "write", arguments: { confirmToken: preview.confirmToken } });
  // Deliberately not awaited individually — both calls are in flight before either
  // settles, opening the same window a real concurrent-client race would hit.
  const [r1, r2] = await Promise.all([call(), call()]);

  const results = [r1, r2];
  const successes = results.filter((r) => !r.isError);
  const failures = results.filter((r) => r.isError);
  expect(successes).toHaveLength(1);
  expect(failures).toHaveLength(1);
  expect(textOf(failures[0]!)).toMatch(/confirm token/i);
});
