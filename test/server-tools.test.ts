// ---- a fully-controllable stub JobManagerApi -------------------------------

class StubJobManager implements JobManagerApi {
  private st: JobStatus = { state: "idle" };
  private res: JobResult | null = null;
  startCalls: { config: MineConfig; deps: JobDeps }[] = [];
  cancelCalls: string[] = [];
  private jobCounter = 0;

  start(config: MineConfig, deps: JobDeps): { jobId: string; resumed: boolean } {
    this.startCalls.push({ config, deps });
    const jobId = `job-${++this.jobCounter}`;
    this.st = { state: "running", jobId, stage: "fetching", counters: {}, warnings: [], tokensSpentUsd: 0 };
    this.res = null;
    return { jobId, resumed: false };
  }

  status(jobId?: string): JobStatus {
    if (jobId !== undefined && jobId !== this.st.jobId) return { state: "idle" };
    return this.st;
  }

  async cancel(jobId: string): Promise<{ checkpointed: boolean }> {
    this.cancelCalls.push(jobId);
    if (this.st.jobId !== jobId) return { checkpointed: false };
    this.st = { ...this.st, state: "cancelled" };
    return { checkpointed: true };
  }

  result(jobId: string): JobResult | null {
    if (this.st.jobId !== jobId || this.st.state !== "ready-for-preview") return null;
    return this.res;
  }

  /** Test-only: simulates the pipeline reaching ready-for-preview. */
  completeToReady(result: JobResult): void {
    this.res = result;
    this.st = { ...this.st, state: "ready-for-preview", stage: "ready-for-preview" };
  }
}

function mkRule(overrides: Partial<RuleRecord> = {}): RuleRecord {
  return {
    id: "1",
    statement: "Always write tests first",
    category: "testing",
    polarity: "prescriptive",
    scope: [],
    score: 0.9,
    verdict: "corroborated",
    evidence: [],
    exemplars: [],
    lastCorroborated: "2026-06-01T00:00:00Z",
    ...overrides,
  };
}

function mkContested(overrides: Partial<ContestedItem> = {}): ContestedItem {
  return {
    id: "c1",
    statement: "Use tabs, not spaces",
    reason: "conflicting guidance",
    sides: [],
    ...overrides,
  };
}

function mkProvenance(rules: RuleRecord[], contested: ContestedItem[] = []): Provenance {
  return ProvenanceSchema.parse({
    generatedAt: "2026-07-01T00:00:00Z",
    intent: "help contributors",
    rules,
    dropped: [],
    contested,
  });
}

function mkDraft(contested: ContestedItem[]): string {
  const lines = ["<!-- generated by prlore -->", "# Conventions", "", "auto-generated", "", "## Testing", "- **Always write tests first**", ""];
  if (contested.length > 0) {
    lines.push("## Needs your call (contested)");
    for (const item of contested) lines.push(`- ${item.statement} — ${item.reason}`);
  }
  return lines.join("\n").replace(/\n+$/, "") + "\n";
}

// StubJobManager (and the literal JobManagerApi some tests build inline) never runs
// the pipeline, so `deps.transport`/`deps.provider` are recorded but never invoked —
// these stand-ins exist only so `mine` doesn't fall through to defaultMineDepsFactory's
// real selectProvider/hasClaudeCli probe of process.env/PATH (which flakes on a
// machine with neither ANTHROPIC_API_KEY nor the claude CLI). Shaped as real
// functions, not bare `{}`, because the happy-path test asserts
// `typeof deps.transport === "function"` and `typeof deps.provider.spentUsd ===
// "function"`.
const stubTransport: GqlTransport = async <T>(): Promise<T> => {
  throw new Error("stub transport should never be called in server-tools tests");
};
const stubProvider: ModelProvider = {
  spentUsd: () => 0,
  async complete() {
    throw new Error("stub provider should never be called in server-tools tests");
  },
};
const stubDepsFactory: MineDepsFactory = (_config, { repoPath, stateDir }) => ({
  transport: stubTransport,
  provider: stubProvider,
  stateDir,
  repoPath,
});

async function connectedClient(manager?: JobManagerApi) {
  const server = manager ? buildServer(manager, stubDepsFactory) : buildServer();
  const client = new Client({ name: "test-client", version: "0.0.0" });
  const [clientTransport, serverTransport] = InMemoryTransport.createLinkedPair();
  await Promise.all([server.connect(serverTransport), client.connect(clientTransport)]);
  return client;
}

function textOf(res: { content?: unknown }): string {
  const [first] = res.content as { type: string; text: string }[];
  return first!.text;
}

