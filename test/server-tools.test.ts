// ---- a fully-controllable stub JobManagerApi -------------------------------

class StubJobManager implements JobManagerApi {
  private st: JobStatus = { state: "idle" };
  private res: JobResult | null = null;
  startCalls: { config: MineConfig; deps: JobDeps }[] = [];
  cancelCalls: string[] = [];
  private jobCounter = 0;

  start(config: MineConfig, deps: JobDeps): { jobId: string; resumed: boolean } {
    this.startCalls.push({ config, deps });
    const jobId = `job-${++this.jobCounter}`;
    this.st = { state: "running", jobId, stage: "fetching", counters: {}, warnings: [], tokensSpentUsd: 0 };
    this.res = null;
    return { jobId, resumed: false };
  }

  status(jobId?: string): JobStatus {
    if (jobId !== undefined && jobId !== this.st.jobId) return { state: "idle" };
    return this.st;
  }

  async cancel(jobId: string): Promise<{ checkpointed: boolean }> {
    this.cancelCalls.push(jobId);
    if (this.st.jobId !== jobId) return { checkpointed: false };
    this.st = { ...this.st, state: "cancelled" };
    return { checkpointed: true };
  }

  result(jobId: string): JobResult | null {
    if (this.st.jobId !== jobId || this.st.state !== "ready-for-preview") return null;
    return this.res;
  }

  /** Test-only: simulates the pipeline reaching ready-for-preview. */
  completeToReady(result: JobResult): void {
    this.res = result;
    this.st = { ...this.st, state: "ready-for-preview", stage: "ready-for-preview" };
  }
}

function mkRule(overrides: Partial<RuleRecord> = {}): RuleRecord {
  return {
    id: "1",
    statement: "Always write tests first",
    category: "testing",
    polarity: "prescriptive",
    scope: [],
    score: 0.9,
    verdict: "corroborated",
    evidence: [],
    exemplars: [],
    lastCorroborated: "2026-06-01T00:00:00Z",
    ...overrides,
  };
}

function mkContested(overrides: Partial<ContestedItem> = {}): ContestedItem {
  return {
    id: "c1",
    statement: "Use tabs, not spaces",
    reason: "conflicting guidance",
    sides: [],
    ...overrides,
  };
}

function mkProvenance(rules: RuleRecord[], contested: ContestedItem[] = []): Provenance {
  return ProvenanceSchema.parse({
    generatedAt: "2026-07-01T00:00:00Z",
    intent: "help contributors",
    rules,
    dropped: [],
    contested,
  });
}

function mkDraft(contested: ContestedItem[]): string {
  const lines = ["<!-- generated by prlore -->", "# Conventions", "", "auto-generated", "", "## Testing", "- **Always write tests first**", ""];
  if (contested.length > 0) {
    lines.push("## Needs your call (contested)");
    for (const item of contested) lines.push(`- ${item.statement} — ${item.reason}`);
  }
  return lines.join("\n").replace(/\n+$/, "") + "\n";
}

async function connectedClient(manager?: JobManagerApi) {
  const server = manager ? buildServer(manager) : buildServer();
  const client = new Client({ name: "test-client", version: "0.0.0" });
  const [clientTransport, serverTransport] = InMemoryTransport.createLinkedPair();
  await Promise.all([server.connect(serverTransport), client.connect(clientTransport)]);
  return client;
}

function textOf(res: { content?: unknown }): string {
  const [first] = res.content as { type: string; text: string }[];
  return first!.text;
}

const tmpRepo = () => mkdtemp(join(tmpdir(), "prlore-server-tools-"));

// ---- Phase 1 compatibility: default construction ---------------------------

test("buildServer() with no args still boots (Phase 1 default-construction contract)", async () => {
  const client = await connectedClient();
  const { tools } = await client.listTools();
  expect(tools.map((t) => t.name)).toEqual(expect.arrayContaining(["mine", "status", "cancel", "preview", "write"]));
});

// ---- happy path: mine -> status -> preview -> write -------------------------

test("mine -> status -> preview -> write happy path", async () => {
  const repoPath = await tmpRepo();
  const manager = new StubJobManager();
  const client = await connectedClient(manager);

  const mineRes = await client.callTool({
    name: "mine",
    arguments: { repo: "octo/repo", intent: "help new contributors", repoPath },
  });
  expect(mineRes.isError).toBeFalsy();
  const { jobId, resumed } = JSON.parse(textOf(mineRes));
  expect(typeof jobId).toBe("string");
  expect(resumed).toBe(false);

  // mine wired the deps as spec'd: a transport function, a provider with spentUsd(),
  // repoPath/stateDir defaulted.
  expect(manager.startCalls).toHaveLength(1);
  const { deps } = manager.startCalls[0]!;
  expect(typeof deps.transport).toBe("function");
  expect(typeof deps.provider.spentUsd).toBe("function");
  expect(deps.repoPath).toBe(repoPath);
  expect(deps.stateDir).toBe(join(repoPath, ".prlore"));

  const statusRes = await client.callTool({ name: "status", arguments: { jobId } });
  const status = JSON.parse(textOf(statusRes));
  expect(status.state).toBe("running");
  expect(status.jobId).toBe(jobId);

  // fake pipeline reaches ready-for-preview
  const rule = mkRule();
  const provenance = mkProvenance([rule]);
  manager.completeToReady({ draft: mkDraft([]), provenance, contested: [] });

  const previewRes = await client.callTool({ name: "preview", arguments: {} });
  expect(previewRes.isError).toBeFalsy();
  const preview = JSON.parse(textOf(previewRes));
  expect(preview.markdown).toContain("Always write tests first");
  expect(preview.stats).toEqual({ rulesIncluded: 1, droppedCount: 0, contestedCount: 0 });
  expect(preview.targetExists).toBe(false);
  expect(preview.wouldRefuse).toBe(false);
  expect(typeof preview.confirmToken).toBe("string");

  const writeRes = await client.callTool({ name: "write", arguments: { confirmToken: preview.confirmToken } });
  expect(writeRes.isError).toBeFalsy();
  const writeOut = JSON.parse(textOf(writeRes));
  expect(writeOut.pathsWritten).toContain(join(repoPath, "AGENTS.md"));

  const written = await readFile(join(repoPath, "AGENTS.md"), "utf8");
  expect(written).toContain("Always write tests first");
});

