import { sanitize } from "../emitter/markers.js";
import type { MineConfig } from "../schemas/mine-config.js";
import type { ContestedItem, RuleRecord } from "../schemas/provenance.js";
import type { DocPlan } from "./select.js";

const MAX_CITATIONS = 3;

// v0.3 Task 4: the heading for the compact-tail section (rules beyond
// output.maxRules). Static, not model-controlled — unlike CONTESTED_HEADER
// there's no untrusted-heading collision risk to guard against here.
export const ADDITIONAL_CONVENTIONS_HEADER = "Additional conventions (lower signal)";

// The literal text of the contested-section header (see the "## Needs your call
// (contested)" push below). Exported so consumers that need to recognize the
// genuine header line (e.g. src/emitter/finalize.ts's finalizeDraft, which anchors its
// contested-section strip to a full-line match of this text) derive it from the
// same source of truth instead of hardcoding a second copy that could drift.
export const CONTESTED_HEADER = "Needs your call (contested)";

// A DocPlan section heading is model-controlled, untrusted text (see planDoc in
// select.ts — the LLM chooses it, and select.ts's enforce() only validates rule
// ids, not heading content). It's rendered directly as its own "## <heading>"
// line below. If its sanitized form happened to equal CONTESTED_HEADER exactly,
// the rendered document would contain two lines that read identically as
// "## Needs your call (contested)": this poisoned section heading (which always
// renders earlier, since sections come before the contested block) and the
// genuine header. A consumer that locates "the" contested header by the first
// full-line match (finalizeDraft in src/emitter/finalize.ts) would then anchor to the
// poisoned line and treat everything after it — every later section plus the
// real contested block — as past the cut point, silently dropping it from the
// written file (a fail-closed under-write: preview shows more than gets
// written). This is the single place every section heading renders through, so
// guarding here closes the hole for any caller, present or future. Perturbing
// with a deterministic, human-legible prefix (rather than rejecting the whole
// plan) keeps a plan usable even if a model picks this exact heading text.
function guardSectionHeading(heading: string): string {
  return heading === CONTESTED_HEADER ? `Section: ${heading}` : heading;
}

function citationsFor(rule: RuleRecord, config: MineConfig): string {
  if (config.output.citations !== "inline-light") return "";
  const prs = [...new Set(rule.evidence.filter((e) => e.verified).map((e) => e.pr))].sort((a, b) => a - b);
  if (prs.length === 0) return "";
  const shown = prs.slice(0, MAX_CITATIONS);
  return ` _(${shown.map((n) => `#${n}`).join(", ")})_`;
}

function renderBullet(rule: RuleRecord, config: MineConfig): string {
  const statement = sanitize(rule.statement);
  const prefix = rule.verdict === "trending-toward"
    ? `- **[migration in progress]** ${statement}`
    : rule.verdict === "trending-away"
      ? `- **[fading]** ${statement}` // survived scoring but is on its way out — must not read as settled
      : `- **${statement}**`;
  const rationale = rule.rationale ? ` — ${sanitize(rule.rationale)}` : "";
  const citations = citationsFor(rule, config);
  return `${prefix}${rationale}${citations}`;
}

// v0.3 Task 4: the compact-tail form for rules beyond output.maxRules —
// statement + citations only, no rationale prose and no verdict prefix. The
// rationale is deliberately dropped: it's where the bytes are, and the whole
// point of the tail is to give these lower-signal rules a cheap mention rather
// than the full section treatment.
function renderCompactBullet(rule: RuleRecord, config: MineConfig): string {
  const statement = sanitize(rule.statement);
  const citations = citationsFor(rule, config);
  return `- ${statement}${citations}`;
}

export function renderDraft(
  plan: DocPlan,
  rules: RuleRecord[],
  contested: ContestedItem[],
  config: MineConfig,
  tailRules: RuleRecord[] = [],
): string {
  const ruleById = new Map(rules.map((r) => [r.id, r]));
  const lines: string[] = [];
  // Tracks every rule id already rendered by a planned section, so the compact
  // tail below can never re-render one even if a future caller passes
  // overlapping `rules`/`tailRules` arrays. The structural guarantee that a
  // rule is EITHER in a section OR in the tail lives one layer up, at the
  // synthesize.ts seam (planDoc only ever sees the top-maxRules slice, so a
  // tail rule's id can never appear in `plan.sections` to begin with) — this
  // set is belt-and-braces defense-in-depth against that invariant being
  // violated by a caller, not the primary mechanism.
  const renderedIds = new Set<string>();

  lines.push("<!-- generated by prlore -->");
  lines.push(`# ${sanitize(plan.title)}`);
  lines.push("");
  lines.push(sanitize(plan.overview));
  lines.push("");

  for (const section of plan.sections) {
    lines.push(`## ${guardSectionHeading(sanitize(section.heading))}`);
    for (const id of section.ruleIds) {
      const rule = ruleById.get(id);
      if (!rule) continue; // enforced plans should never carry unknown ids; defensive only
      lines.push(renderBullet(rule, config));
      renderedIds.add(id);
    }
    lines.push("");
  }

  // Compact tail (v0.3 Task 4): renders INSIDE the managed block, before the
  // contested section, as ONE trailing section. Absent entirely when there's
  // nothing to put in it (rule count <= output.maxRules).
  const compactTail = tailRules.filter((r) => !renderedIds.has(r.id));
  if (compactTail.length > 0) {
    lines.push(`## ${ADDITIONAL_CONVENTIONS_HEADER}`);
    for (const rule of compactTail) {
      lines.push(renderCompactBullet(rule, config));
      renderedIds.add(rule.id);
    }
    lines.push("");
  }

  if (contested.length > 0) {
    lines.push(`## ${CONTESTED_HEADER}`);
    for (const item of contested) {
      lines.push(`- ${sanitize(item.statement)} — ${sanitize(item.reason)}`);
    }
  }

  // Normalize EOF to exactly one trailing newline, regardless of how the last
  // section/contested-block left the lines array.
  return lines.join("\n").replace(/\n+$/, "") + "\n";
}
